// ─────────────────────────────────────────── runtime cross-check

/// Derive the `(neighbor_face, rotation)` pair for a given
/// `(current_face, exit_edge)` purely from the face-basis vectors.
///
/// This is the reference implementation that [`SEAM_TABLE`]'s
/// hard-coded entries are verified against — call it from tests and
/// debug tools; prefer [`SEAM_TABLE`] in hot code paths.
pub fn seam_rotation(current: Face, exit_edge: u8) -> (Face, [[f32; 3]; 3]) {
    let (u_cur, v_cur) = current.tangents();
    let n_cur = current.normal();
    // body-space direction the ray is heading in as it crosses the edge
    let body_dir: [f64; 3] = match exit_edge {
        0 => [-u_cur[0] as f64, -u_cur[1] as f64, -u_cur[2] as f64],
        1 => [u_cur[0] as f64, u_cur[1] as f64, u_cur[2] as f64],
        2 => [-v_cur[0] as f64, -v_cur[1] as f64, -v_cur[2] as f64],
        3 => [v_cur[0] as f64, v_cur[1] as f64, v_cur[2] as f64],
        _ => panic!("invalid exit edge {exit_edge}"),
    };
    // Neighbor face is the one whose normal points in body_dir.
    let neighbor = Face::ALL
        .iter()
        .copied()
        .find(|f| {
            let n = f.normal();
            (n[0] as f64 - body_dir[0]).abs() < 1.0e-6
                && (n[1] as f64 - body_dir[1]).abs() < 1.0e-6
                && (n[2] as f64 - body_dir[2]).abs() < 1.0e-6
        })
        .expect("every unit body-axis must be a face normal");

    // R_seam = R_neighbor · R_current^T.
    // Using rows u_cur, v_cur, n_cur for R_current, so R_current^T's
    // columns are those same rows.
    let r_cur = [
        [u_cur[0] as f64, u_cur[1] as f64, u_cur[2] as f64],
        [v_cur[0] as f64, v_cur[1] as f64, v_cur[2] as f64],
        [n_cur[0] as f64, n_cur[1] as f64, n_cur[2] as f64],
    ];
    let (u_nbr, v_nbr) = neighbor.tangents();
    let n_nbr = neighbor.normal();
    let r_nbr = [
        [u_nbr[0] as f64, u_nbr[1] as f64, u_nbr[2] as f64],
        [v_nbr[0] as f64, v_nbr[1] as f64, v_nbr[2] as f64],
        [n_nbr[0] as f64, n_nbr[1] as f64, n_nbr[2] as f64],
    ];

    // R_cur_T [i][j] = R_cur [j][i]
    let mut r_seam = [[0.0f64; 3]; 3];
    for i in 0..3 {
        for j in 0..3 {
            // (R_nbr · R_cur^T)[i][j] = Σ_k R_nbr[i][k] · R_cur[j][k]
            let mut sum = 0.0;
            for k in 0..3 {
                sum += r_nbr[i][k] * r_cur[j][k];
            }
            r_seam[i][j] = sum;
        }
    }

    let mut out = [[0.0f32; 3]; 3];
    for i in 0..3 {
        for j in 0..3 {
            out[i][j] = r_seam[i][j] as f32;
        }
    }

    (neighbor, out)
}

/// Given a forward seam crossing `(current, exit_edge)` that enters
/// `neighbor_face`, return the edge of `neighbor_face` whose outward
/// body direction points back at `current` — i.e. the edge a ray
/// would exit through if it reversed course and crossed the same
/// cube edge in the opposite direction. Used by the round-trip test.
pub fn reverse_edge(current: Face, exit_edge: u8) -> u8 {
    let (u_cur, v_cur) = current.tangents();
    let body_dir: [f32; 3] = match exit_edge {
        0 => [-u_cur[0], -u_cur[1], -u_cur[2]],
        1 => [u_cur[0], u_cur[1], u_cur[2]],
        2 => [-v_cur[0], -v_cur[1], -v_cur[2]],
        3 => [v_cur[0], v_cur[1], v_cur[2]],
        _ => panic!("invalid exit edge {exit_edge}"),
    };
    // Find the neighbor face.
    let neighbor = Face::ALL
        .iter()
        .copied()
        .find(|f| {
            let n = f.normal();
            (n[0] - body_dir[0]).abs() < 1.0e-5
                && (n[1] - body_dir[1]).abs() < 1.0e-5
                && (n[2] - body_dir[2]).abs() < 1.0e-5
        })
        .expect("neighbor exists");
    // Two cube faces share a cube edge, not antiparallel normals.
    // The reverse seam from `neighbor` back to `current` exits along
    // the edge of `neighbor` whose body direction points toward
    // `current`'s center — i.e. equals `n_current`.
    let n_cur = current.normal();
    let ret_dir = n_cur;
    let (u_nbr, v_nbr) = neighbor.tangents();
    // Match ret_dir against the neighbor's four edge body directions.
    // Edge body dirs are: 0=-u_nbr, 1=+u_nbr, 2=-v_nbr, 3=+v_nbr.
    let close = |a: [f32; 3], b: [f32; 3]| {
        (a[0] - b[0]).abs() < 1.0e-5
            && (a[1] - b[1]).abs() < 1.0e-5
            && (a[2] - b[2]).abs() < 1.0e-5
    };
    if close([-u_nbr[0], -u_nbr[1], -u_nbr[2]], ret_dir) {
        0
    } else if close(u_nbr, ret_dir) {
        1
    } else if close([-v_nbr[0], -v_nbr[1], -v_nbr[2]], ret_dir) {
        2
    } else if close(v_nbr, ret_dir) {
        3
    } else {
        panic!(
            "reverse edge not found: current {current:?} exit_edge {exit_edge} \
             neighbor {neighbor:?} ret_dir {ret_dir:?}"
        )
    }
}

