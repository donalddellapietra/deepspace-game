//! Face-seam rotation table for the unified cubed-sphere DDA.
//!
//! When a ray exits a face subtree through one of the four UV edges,
//! it enters a neighbor face's subtree. Moving between faces requires
//! a single 3×3 rotation of `rd_local` (and the residual): the
//! current face's `(u, v, n)` basis rotates into the neighbor face's
//! `(u, v, n)` basis, preserving the shared-edge direction.
//!
//! Both the `(current_face, exit_edge) → (neighbor_face, rotation)`
//! mapping and the rotation matrices themselves are precomputed at
//! compile time here in [`SEAM_TABLE`]. The runtime helper
//! [`seam_rotation`] builds the table from scratch and exists solely
//! as a test-time cross-check — the shader will read [`SEAM_TABLE`]
//! straight out of a uniform.
//!
//! Exit-edge encoding: `0 = -u`, `1 = +u`, `2 = -v`, `3 = +v`.

use super::Face;

/// Neighbor-face + rotation describing one of the 24 (face, edge)
/// seam transitions.
#[derive(Copy, Clone, Debug, PartialEq)]
pub struct SeamTransition {
    /// Face the ray enters after crossing this edge.
    pub neighbor_face: Face,
    /// 3×3 rotation that maps a vector in `current_face`'s `(u, v, n)`
    /// basis into `neighbor_face`'s `(u, v, n)` basis.
    ///
    /// Equal to `R_neighbor · R_current^T`, where `R_face` has the
    /// face's `(u_axis, v_axis, n_axis)` as its rows (so `R_face ·
    /// body_vec = face_vec`).
    pub rotation: [[f32; 3]; 3],
}

